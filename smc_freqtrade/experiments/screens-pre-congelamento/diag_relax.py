import pandas as pd, numpy as np
df = pd.read_parquet("user_data/diag_df.parquet")
def col(n): return df[n].fillna(False).to_numpy(bool) if n in df.columns else np.zeros(len(df), bool)
o=df["open"].to_numpy(float); h=df["high"].to_numpy(float); lo=df["low"].to_numpy(float); c=df["close"].to_numpy(float)
st=df["setup_state"].astype("string").fillna("").to_numpy()
dr=df["setup_direction"].astype("string").fillna("").to_numpy()
choch_b=col("choch_internal_bullish"); choch_s=col("choch_internal_bearish")
sweep_b=col("sweep_bullish_wick")|col("sweep_bullish_retest")
sweep_s=col("sweep_bearish_wick")|col("sweep_bearish_retest")
rng=h-lo; safe=rng>0; rs=np.where(safe,rng,1.0)
ub=np.minimum(o,c); lb=np.maximum(o,c)
rej_b=safe&((ub-lo)/rs>=0.5)&(c>=lo+0.667*rng)
rej_s=safe&((h-lb)/rs>=0.5)&(c<=h-0.667*rng)
def rec(a,k=16): return pd.Series(a).rolling(k,min_periods=1).max().fillna(0).to_numpy(bool)
now_b=choch_b&rej_b&rec(sweep_b); now_s=choch_s&rej_s&rec(sweep_s)
win_b=rec(choch_b)&rec(rej_b)&rec(sweep_b); win_s=rec(choch_s)&rec(rej_s)&rec(sweep_s)
pl=(st=="PENDING_CONFIRMATION")&(dr=="long"); ps=(st=="PENDING_CONFIRMATION")&(dr=="short")
print(f"PENDING long={int(pl.sum())}  short={int(ps.sum())}")
print(f"confirma ATUAL  (mesmo candle): long={int((pl&now_b).sum())}  short={int((ps&now_s).sum())}")
print(f"confirma RELAXADO (janela 16):  long={int((pl&win_b).sum())}  short={int((ps&win_s).sum())}")
